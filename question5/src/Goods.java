public class Goods {
    private String book;
    private String snake;

    public Goods(String book,String snake)
    {
        this.book=book;
        this.snake=snake;
    }
    public void setBook(String book) {
        this.book = book;
    }

    public void setSnake(String snake) {
        this.snake = snake;
    }

    public String getBook() {
        return book;
    }

    public String getSnake() {
        return snake;
    }

}
